

# RANGE FUNCTION :

# syntax --> range (start, stop, step_size)

# **************************************************************

# to start from 0 :

for i in range(8):
    # OUTPUT : 0 to 7
    print(i)

print("\n")
# **************************************************************

# to start from 1 :

for j in range(1, 8):
    # OUTPUT : 1 to 7
    print(j)

print("\n")
# **************************************************************

for c in range(1, 8+1):
    # OUTPUT : 1 to 8
    print(c)

print("\n")

# **************************************************************
# to start, stop, step :

for k in range(1, 10, 2):
    print(k)


print("\n")
# **************************************************************
