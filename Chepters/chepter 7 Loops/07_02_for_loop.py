
# to use FOR LOOP we use "range" for NUMBERS. Here we use Range thus no need for i=i+1 kinda clarification like "while loop" use

for i in range(10):
    print(i)

# to use FOR LOOP we use "item" for LIST

fruits = ["apple", "banana", "mango"]

for item in fruits:
    print(item)  # it will print only item by using loop

for item in fruits:
    print(fruits)  # it will print whole list with bracket 3 times coz here 3 items
