# assignment operators

c = 10
c += 2  # same as c language
c -= 2  # same as c language
c *= 2  # same as c language
c /= 2  # same as c language
print(c)

# comparison operators

d1 = (14 == 7)
print(d1)
d2 = (14 < 7)
print(d2)
d3 = (14 > 7)  # true
print(d3)
d4 = (14 != 7)  # true
print(d4)

# logical operators

